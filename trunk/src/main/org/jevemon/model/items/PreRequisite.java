package org.jevemon.model.items;

public class PreRequisite {

	private int typeID;
	private int level;
	
	public PreRequisite(){
		
	}
	
	
	
	public int getTypeID() {
		return typeID;
	}
	public int getLevel() {
		return level;
	}
	public void setTypeID(int typeID) {
		this.typeID = typeID;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	
	
}
