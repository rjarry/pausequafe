#include "AboutPQ.h"
#include "ui_AboutPQ.h"

AboutPQ::AboutPQ(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::AboutPQ)
{
    ui->setupUi(this);
}

AboutPQ::~AboutPQ()
{
    delete ui;
}

void AboutPQ::changeEvent(QEvent *e)
{
    QDialog::changeEvent(e);
    switch (e->type()) {
    case QEvent::LanguageChange:
        ui->retranslateUi(this);
        break;
    default:
        break;
    }
}
